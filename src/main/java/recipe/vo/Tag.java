package recipe.vo;

public class Tag {
  private int tno;
  private String tname;
  
  public Tag() {}
  
  public int getTno() {
    return tno;
  }
  public void setTno(int tno) {
    this.tno = tno;
  }
  public String getTname() {
    return tname;
  }
  public void setTname(String tname) {
    this.tname = tname;
  }
  
  
}