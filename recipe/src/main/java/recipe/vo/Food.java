package recipe.vo;

public class Food {
  int bno;
  int tno;

  public Food() {
    super();
  }

  public int getBno() {
    return bno;
  }

  public void setBno(int bno) {
    this.bno = bno;
  }

  public int getTno() {
    return tno;
  }

  public void setTno(int tno) {
    this.tno = tno;
  }

}